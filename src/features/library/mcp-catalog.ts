import type { McpTransport } from "@/lib/api";

/**
 * A curated, recommended MCP server. These are starting points: selecting one
 * prefills the editor with a sensible default config plus blank credential
 * fields the user fills in. Everything stays editable before saving — Grex
 * never runs these automatically.
 *
 * `envKeys` / `headerKeys` are the credentials a server needs — rendered as
 * empty `KEY=` / `Key:` rows so the user knows exactly what to provide.
 * `iconKey` maps to a brand glyph in `mcp-brand-icon`; omit for a letter badge.
 */
