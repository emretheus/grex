const WORKSPACE_STAR_INDEX = new Map<string, number>(
	WORKSPACE_STAR_NAMES.map((name, index) => [name, index + 1]),
);

export function formatWorkspaceStarProgress(workspaceName: string): string {
	const normalized = workspaceName
		.trim()
		.toLowerCase()
		.replace(/-v\d+$/, "");
	const current = WORKSPACE_STAR_INDEX.get(normalized) ?? 0;

	return `You've collected ${current}/${WORKSPACE_STAR_NAMES.length} stars!`;
}
